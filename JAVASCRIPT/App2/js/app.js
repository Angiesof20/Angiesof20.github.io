//Defino las Variables
var base,altura,area;

//Capturar los datos del rectangulo... Entrada
base=parseFloat(prompt("Ingrese la base del rectangulo"));
altura=parseFloat(prompt("Ingrese la altura del rectangulo"));

//Proceso del Area
area=base*altura;

//Mensaje de Salida
document.write("El area del rectangulo es: " +area );